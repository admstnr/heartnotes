var Actions = exports.Actions = {};

actionTypes.forEach(function(type) {
  Actions[type] = type;
});



export function buildAction(type, payload = {}) {
  if (payload && payload instanceof Error) {
    payload = {
      error: payload
    };
  }

  return {
    type: type,
    payload: payload,
  };
};



